def run_mystats(epicsfile) :
    
    pvlist = 'HD:coda:daq:status,HD:coda:daq:run_number,HD:coda:EventNumber'

    if os.path.exists(epicsfile):
        os.remove(epicsfile)

    cmd = "myStats -b -10s -e 0 -u -f " + epicsfile + " -l " + pvlist

    os.system(cmd)
    
    return

