def read_mystats(epicsfile) :

    f = open(epicsfile,'r')
    lines = f.readlines()
    f.close()

    dictlist = {}    
    # Name     Min   Mean   Max   Sigma
    
    for line in lines:
 
        things = line.strip().split(" ")
        pvname = things[0]
            
        if things[3] == 'N/A' :
            max = -1
        elif things[3] == '<undefined>' :
            max = -1
        else :
            max = things[3]        

        if things[4] == 'N/A' :
            sigma = -1
        elif things[4] == '<undefined>' :
            sigma = -1
        else :
            sigma = float(things[4])        

        dict = {pvname : { "max":max, "sigma":sigma}}
        dictlist.update(dict)
        
    return dictlist


